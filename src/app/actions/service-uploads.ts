'use server';

import { supabase } from '@/lib/supabase';
import { db } from '@/lib/db';
import { revalidatePath } from 'next/cache';

export async function getServiceUploads(studentId: string, serviceType: string) {
    try {
        const uploads = await db.serviceUploads.getByStudent(studentId, serviceType);
        return { success: true, data: uploads };
    } catch (error: any) {
        console.error('Error fetching service uploads:', error);
        return { success: false, error: error.message };
    }
}

export async function uploadServiceFile(formData: FormData) {
    try {
        const file = formData.get('file') as File;
        const studentId = formData.get('studentId') as string;
        const serviceType = formData.get('serviceType') as string;

        if (!file || !studentId || !serviceType) {
            throw new Error('Missing required fields');
        }

        // Check file count limit
        const existingUploads = await db.serviceUploads.getByStudent(studentId, serviceType);
        if (existingUploads.length >= 10) {
            throw new Error('Dosya yükleme limiti (10) doldu.');
        }

        const fileExt = file.name.split('.').pop();
        const fileName = `${studentId}-${serviceType}-${Date.now()}.${fileExt}`;
        const filePath = `service-uploads/${fileName}`;

        const arrayBuffer = await file.arrayBuffer();

        // Upload to Supabase
        const { error: uploadError } = await supabase.storage
            .from('service-uploads')
            .upload(filePath, arrayBuffer, {
                contentType: file.type,
                upsert: false
            });

        if (uploadError) {
            // Try to create bucket if it doesn't exist (similar to student-photo logic)
            if ((uploadError as any).error === 'Bucket not found' || uploadError.message === 'Bucket not found') {
                await supabase.storage.createBucket('service-uploads', {
                    public: true,
                    fileSizeLimit: 10485760, // 10MB
                    allowedMimeTypes: ['image/*', 'application/pdf', 'application/msword', 'application/vnd.openxmlformats-officedocument.wordprocessingml.document']
                });

                // Retry
                const { error: retryError } = await supabase.storage
                    .from('service-uploads')
                    .upload(filePath, arrayBuffer, {
                        contentType: file.type,
                        upsert: false
                    });
                if (retryError) throw retryError;
            } else {
                throw uploadError;
            }
        }

        const { data: { publicUrl } } = supabase.storage
            .from('service-uploads')
            .getPublicUrl(filePath);

        // Save to DB
        await db.serviceUploads.create({
            id: '', // Generated by DB
            studentId,
            serviceType,
            fileUrl: publicUrl,
            fileName: file.name,
            fileSize: file.size,
            fileType: file.type,
            uploadedAt: new Date().toISOString()
        });

        revalidatePath('/branch/accommodation');
        revalidatePath('/branch/life-support');
        revalidatePath('/italy/accommodation');
        revalidatePath('/italy/life-support');

        return { success: true };
    } catch (error: any) {
        console.error('Error uploading service file:', error);
        return { success: false, error: error.message };
    }
}

